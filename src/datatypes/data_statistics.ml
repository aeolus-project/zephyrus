(** Statistics. *)

type statistic = string * string

let statistics = ref []

let add key value = 
  statistics := (key, value)::!statistics

let to_string () =
  let lines = List.map (fun (key, value) -> Printf.sprintf "%s:%s" key value) (List.rev !statistics) in
  String.concat "\n" lines